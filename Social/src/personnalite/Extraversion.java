package personnalite;

public class Extraversion {
	private String extraversion;

	public Extraversion(String extraversion) {
		this.extraversion = extraversion;
	}

	public String getExtraversion() {
		return extraversion;
	}

	@Override
	public String toString() {
		return "Extraversion [extraversion=" + extraversion + "]";
	}
	
	

}
