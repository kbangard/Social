package personnalite;

public class Agreabilite {
	private String agreabilite;

	public Agreabilite(String agreabilite) {
		this.agreabilite = agreabilite;
	}

	public String getAgreabilite() {
		return agreabilite;
	}

	@Override
	public String toString() {
		return "Agreabilite [agreabilite=" + agreabilite + "]";
	}
	
	

}
